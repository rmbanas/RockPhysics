            m1.Series.Add(CreateLineSeries(rm.phi, rm.ksat, "KSat (upper)", OxyColors.SaddleBrown));
            m1.Series.Add(CreateLineSeries(rm2.phi, rm2.ksat, "KSat (Contact Cement)", OxyColors.SeaGreen));
            m1.Series.Add(CreateLineSeries(rm2.phi, rm2.gdry, "G (Contact Cement)", OxyColors.Orange));
            m1.InvalidatePlot(true);

            m2.Series.Clear();
            m2.Series.Add(CreateLineSeries(rm.phi, rm.vp, "FS Vp", OxyColors.Black));
            m2.Series.Add(CreateLineSeries(rm2.phi, rm2.vp, "CEM Vp", OxyColors.Red));
            m2.InvalidatePlot(true);

            m3.Series.Clear();
            //m3.Series.Add(CreateScatterSeries(rm.ai, rm.pr, "Friable", OxyColors.Black));
            //m3.Series.Add(CreateScatterSeries(rm2.ai, rm2.pr, "Cem", OxyColors.Red));

            //AddTrainingData(m3);

            // Assign which rock model you want here:
            RockModel rtemp = rm2;

            int ns = rtemp.ns;
            double[] phi = rtemp.phi;
            double[] k = rtemp.kdry;
            double[] g = rtemp.gdry;

            for (int i = 0; i < ns; i++)
            {
                if (Math.Round(phi[i] * 100, 0) % 5 == 0)
                {
                    var l1 = new List<double>();
                    var l2 = new List<double>();
                    
                    for (double s = 0; s <= 1; s += 0.05)
                    {
                        // Create new rhob using the new fluids and grain density
                        double rhob = (1 - phi[i]) * rtemp.gd + phi[i] * rtemp.fluid.density_voight(s, 0, 1 - s);
                        double vs = Math.Sqrt(g[i] / rhob);
                        double ksat = rtemp.K_sat(k[i], phi[i], gcv("Kmin"), rm.fluid.homo(s, 0, 1 - s));
                        double vp = Math.Sqrt((ksat + 4 * g[i] / 3) / rhob);
                        double vpvs = vp / vs;
                        double pr = 0.5 * (Math.Pow(vpvs, 2) - 2) / (Math.Pow(vpvs, 2) - 1);
                        l1.Add(vp * rhob);
                        l2.Add(pr);
                    }

                    m3.Series.Add(CreateLineSeries(l1.ToArray(), l2.ToArray(), "PHI=" + phi[i].ToString(), OxyColors.Automatic, LineStyle.Automatic, 5));
                }
            }

            m3.InvalidatePlot(true);

        }

        /// <summary>
        /// Generate a line series for a given set of arrays, specifying color and linestyle, etc.
        /// </summary>
        /// <param name="x"></param>
        /// <param name="y"></param>
        /// <param name="name"></param>
        /// <param name="color"></param>
        /// <param name="ls"></param>
        /// <param name="st"></param>
        /// <returns></returns>
        private LineSeries CreateLineSeries(double[] x, double[] y, string name, OxyColor color, LineStyle ls = LineStyle.Automatic, double st = 2)
        {
            var s1 = new LineSeries()
            {
                Title = name,
                Color = color,
                LineStyle = ls,
                StrokeThickness = st
            };


            for (int i = 0; i < x.Length; i++)
            {
                s1.Points.Add(new DataPoint(x[i], y[i]));
            }

            return s1;
        }


        /// <summary>
        /// Create a scatter series for two given arrays with parameters
        /// </summary>
        /// <param name="x"></param>
        /// <param name="y"></param>
        /// <param name="name"></param>
        /// <param name="color"></param>
        /// <returns></returns>
        private ScatterSeries CreateScatterSeries(double[] x, double[] y, string name, OxyColor color)
        {
            var s1 = new ScatterSeries()
            {
                Title = name,
                MarkerType = MarkerType.Circle,
                MarkerSize = 1,
                MarkerFill = color
            };


            for (int i = 0; i < x.Length; i++)
            {
                s1.Points.Add(new ScatterPoint(x[i], y[i]));
            }

            return s1;
        }

