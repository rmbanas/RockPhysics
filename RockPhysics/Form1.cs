        /// <summary>
        /// Retrieve value from a given control by name
        /// </summary>
        /// <param name="name"></param>
        /// <returns></returns>
        private double gcv(string name)
        {
            foreach (Control c in panel1.Controls)
            {
                if (c.GetType() == typeof(Field))
                {
                    if (((Field)c).label.Text == name)
                    {
                        return Convert.ToDouble(((Field)c).text_box.Text);
                    }
                }
            }
            return 0;
        }

